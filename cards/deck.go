type deck []string

func newDeck() deck {
	values := deck{"Ace", "Two", "Three", "Four"}

	suits := deck{"Spades", "Diamonds", "Clubs", "Hearts"}

	var cards deck

	for _, value := range values {
		for _, suit := range suits {
			cards = append(cards, value+" of "+suit)
		}
	}

	return cards
}

